# Палиндром - это слово или текст, которые читаются назад и вперед.
# Создайте программу, которая проверяет, является ли слово палиндромом.

# Формат ввода:
# Слово, которое нужно проверить. Гарантируется, что слово будет четной длины.

# Формат вывода:
# Если слово палиндром, напишите Palindrome. В противном случае напишите Not palindrome

# Sample Input 1:
# noon

# Sample Output 1:
# Palindrome

# Sample Input 2:
# banana

# Sample Output 2:
# Not palindrome

